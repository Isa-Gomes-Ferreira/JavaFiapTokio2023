package logica.aulas.aula07;

public class ForLaco {

	public static void main(String[] args) {

		System.out.println("Incremento: ");
		for(int i = 0; i < 5; i++) {
			System.out.println("Cambalhota " + i);
		}
		
		System.out.println("Decremento: ");
		for(int i = 5; i >= 0; i--) {
			System.out.println("Cambalhota " + i);
		}
		
	}

}
